package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/8/24.
 */
public class OrderSettlementInfo implements Serializable {

    private String id; // 用户拥有的红包ID
    private String bonusName; //红包名称
    private String minOrderAmount;// 最少消费标准
    private String bonusDiscount;// 红包抵扣金额

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBonusName() {
        return bonusName;
    }

    public void setBonusName(String bonusName) {
        this.bonusName = bonusName;
    }

    public String getMinOrderAmount() {
        return minOrderAmount;
    }

    public void setMinOrderAmount(String minOrderAmount) {
        this.minOrderAmount = minOrderAmount;
    }

    public String getBonusDiscount() {
        return bonusDiscount;
    }

    public void setBonusDiscount(String bonusDiscount) {
        this.bonusDiscount = bonusDiscount;
    }
}
