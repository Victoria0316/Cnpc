package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/8/26.
 */
public class BorrowOrdersForSureBean implements Serializable {

    private String productName;//油品名称
    private String customerPrice;//当前价格
    private String borrowMinPurchases;//最小借用量
    private String borrowMinIncremental;//递增最小量
    private String borrowBuyProcedure;//借用手续费

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getCustomerPrice() {
        return customerPrice;
    }

    public void setCustomerPrice(String customerPrice) {
        this.customerPrice = customerPrice;
    }

    public String getBorrowMinPurchases() {
        return borrowMinPurchases;
    }

    public void setBorrowMinPurchases(String borrowMinPurchases) {
        this.borrowMinPurchases = borrowMinPurchases;
    }

    public String getBorrowMinIncremental() {
        return borrowMinIncremental;
    }

    public void setBorrowMinIncremental(String borrowMinIncremental) {
        this.borrowMinIncremental = borrowMinIncremental;
    }

    public String getBorrowBuyProcedure() {
        return borrowBuyProcedure;
    }

    public void setBorrowBuyProcedure(String borrowBuyProcedure) {
        this.borrowBuyProcedure = borrowBuyProcedure;
    }
}
