package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/9/7.
 */
public class BorrowGasDetailInfo implements Serializable {
    private String id;  //主键
    private String oilMoney;  //购买金额
    private String oilName;  //油品名称
    private String oilVolume;  //购买升数
    private String tradeTime;  //交易时间
    private String tradeType;  //交易类型

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOilMoney() {
        return oilMoney;
    }

    public void setOilMoney(String oilMoney) {
        this.oilMoney = oilMoney;
    }

    public String getOilName() {
        return oilName;
    }

    public void setOilName(String oilName) {
        this.oilName = oilName;
    }

    public String getOilVolume() {
        return oilVolume;
    }

    public void setOilVolume(String oilVolume) {
        this.oilVolume = oilVolume;
    }

    public String getTradeTime() {
        return tradeTime;
    }

    public void setTradeTime(String tradeTime) {
        this.tradeTime = tradeTime;
    }

    public String getTradeType() {
        return tradeType;
    }

    public void setTradeType(String tradeType) {
        this.tradeType = tradeType;
    }
}
