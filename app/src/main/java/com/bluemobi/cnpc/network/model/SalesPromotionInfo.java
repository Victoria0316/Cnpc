package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/8/25.
 */
public class SalesPromotionInfo implements Serializable {

    private String actTag; //发布状态
    private String artContent; //文章内容
    private String artTitle;//文章标题
    private String attrDeptCode;//加油站代号
    private String attrDeptId;//加油站D
    private String attrDeptName;//加油站名称
    private String id;//主键id
    private String imagePath;//封面图片地址
    private String releaseDate;//发布时间
    private String releaseState;//过期状态
    private String openState;//开启状态
    private String webLink;//web链接

    public String getActTag() {
        return actTag;
    }

    public void setActTag(String actTag) {
        this.actTag = actTag;
    }

    public String getArtContent() {
        return artContent;
    }

    public void setArtContent(String artContent) {
        this.artContent = artContent;
    }

    public String getArtTitle() {
        return artTitle;
    }

    public void setArtTitle(String artTitle) {
        this.artTitle = artTitle;
    }

    public String getAttrDeptCode() {
        return attrDeptCode;
    }

    public void setAttrDeptCode(String attrDeptCode) {
        this.attrDeptCode = attrDeptCode;
    }

    public String getAttrDeptId() {
        return attrDeptId;
    }

    public void setAttrDeptId(String attrDeptId) {
        this.attrDeptId = attrDeptId;
    }

    public String getAttrDeptName() {
        return attrDeptName;
    }

    public void setAttrDeptName(String attrDeptName) {
        this.attrDeptName = attrDeptName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImagePath() {
        return imagePath;
    }

    public void setImagePath(String imagePath) {
        this.imagePath = imagePath;
    }

    public String getReleaseDate() {
        return releaseDate;
    }

    public void setReleaseDate(String releaseDate) {
        this.releaseDate = releaseDate;
    }

    public String getReleaseState() {
        return releaseState;
    }

    public void setReleaseState(String releaseState) {
        this.releaseState = releaseState;
    }

    public String getOpenState() {
        return openState;
    }

    public void setOpenState(String openState) {
        this.openState = openState;
    }

    public String getWebLink() {
        return webLink;
    }

    public void setWebLink(String webLink) {
        this.webLink = webLink;
    }
}
