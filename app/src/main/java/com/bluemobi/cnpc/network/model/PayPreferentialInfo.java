package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 * Created by gaoyn on 2015/8/20.
 */
public class PayPreferentialInfo implements Serializable {

    private String id;// 套餐主键ID
    private String comboType; //套餐类型（0即时套餐，1账期套餐）
    private String comboName; //套餐名称
    private String depositRate;// 折扣率(%)
    private String buyPrice;// 套餐价格
    private String timeLimit;// 期限(0表示即时可用)月
    private String firstArrive;// 第一月到账(%)
    private String otherArrive;// 其他单月到账(%)
    private String actualAmount;// 实际消费金额
    private String preferentialPrice;// 优惠价格（每份优惠）


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getComboType() {
        return comboType;
    }

    public void setComboType(String comboType) {
        this.comboType = comboType;
    }

    public String getComboName() {
        return comboName;
    }

    public void setComboName(String comboName) {
        this.comboName = comboName;
    }

    public String getDepositRate() {
        return depositRate;
    }

    public void setDepositRate(String depositRate) {
        this.depositRate = depositRate;
    }

    public String getBuyPrice() {
        return buyPrice;
    }

    public void setBuyPrice(String buyPrice) {
        this.buyPrice = buyPrice;
    }

    public String getTimeLimit() {
        return timeLimit;
    }

    public void setTimeLimit(String timeLimit) {
        this.timeLimit = timeLimit;
    }

    public String getFirstArrive() {
        return firstArrive;
    }

    public void setFirstArrive(String firstArrive) {
        this.firstArrive = firstArrive;
    }

    public String getOtherArrive() {
        return otherArrive;
    }

    public void setOtherArrive(String otherArrive) {
        this.otherArrive = otherArrive;
    }

    public String getActualAmount() {
        return actualAmount;
    }

    public void setActualAmount(String actualAmount) {
        this.actualAmount = actualAmount;
    }

    public String getPreferentialPrice() {
        return preferentialPrice;
    }

    public void setPreferentialPrice(String preferentialPrice) {
        this.preferentialPrice = preferentialPrice;
    }
}
