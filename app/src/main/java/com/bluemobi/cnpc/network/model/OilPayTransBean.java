package com.bluemobi.cnpc.network.model;

import java.io.Serializable;

/**
 */
public class OilPayTransBean implements Serializable {


    private String gasID;
    private String gasName;
    private String gasType;
    private String singlePrice;
    private String sumPrice;
    private String productID;

    @Override
    public String toString() {
        return "OilPayTransBean{" +
                "gasID='" + gasID + '\'' +
                ", gasName='" + gasName + '\'' +
                ", gasType='" + gasType + '\'' +
                ", singlePrice='" + singlePrice + '\'' +
                ", sumPrice='" + sumPrice + '\'' +
                ", calOilNum='" + calOilNum + '\'' +
                '}';
    }

    private String calOilNum;

    public String getGasID() {
        return gasID;
    }

    public void setGasID(String gasID) {
        this.gasID = gasID;
    }

    public String getGasName() {
        return gasName;
    }

    public void setGasName(String gasName) {
        this.gasName = gasName;
    }

    public String getGasType() {
        return gasType;
    }

    public void setGasType(String gasType) {
        this.gasType = gasType;
    }

    public String getCalOilNum() {
        return calOilNum;
    }

    public void setCalOilNum(String calOilNum) {
        this.calOilNum = calOilNum;
    }

    public String getSumPrice() {
        return sumPrice;
    }

    public void setSumPrice(String sumPrice) {
        this.sumPrice = sumPrice;
    }

    public String getSinglePrice() {
        return singlePrice;
    }

    public void setSinglePrice(String singlePrice) {
        this.singlePrice = singlePrice;
    }


    public String getProductID() {
        return productID;
    }

    public void setProductID(String productID) {
        this.productID = productID;
    }
}
